// Cedarwings SAS — Purchase Order generation + supplier email.
// Shared by inventory.html (requisition queue) and requisition.html.
//
//   import { createAndEmailPOs } from './po.js'
//   const res = await createAndEmailPOs(sb, items, {source, source_ref, createdBy})
//
// items: [{ item_name, quantity, unit?, unit_cost?, bom_id?, ref_id? }]
// Groups items by each item's preferred supplier, writes one purchase_orders
// row (+ purchase_order_lines) per supplier, emails the PO to the supplier,
// and returns a summary the caller can use to update the source rows.

import { CW_Notify } from './brevo.js'

function esc(s){return String(s==null?'':s).replace(/[&<>"]/g,c=>({'&':'&amp;','<':'&lt;','>':'&gt;','"':'&quot;'}[c]))}
function num(v){const n=Number(v);return Number.isFinite(n)?n:0}

// Default CC for every PO email, set in Settings (key: po_cc_email).
// Supports several addresses separated by comma/semicolon.
async function orderCc(sb){
  const { data } = await sb.from('system_settings').select('value').eq('key','po_cc_email').maybeSingle()
  const raw = (data && data.value) || ''
  const list = raw.split(/[,;]/).map(x=>x.trim()).filter(x=>x.includes('@'))
  return list.length ? list.map(email=>({email})) : null
}

